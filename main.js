function imageup(up) {
    const fileInputId = 'file' + up;
    const idname = "upload_btn" + up;

    document.getElementById(fileInputId).addEventListener('change', function(event) {
        const file = event.target.files[0];
        const reader = new FileReader();
        
        reader.onload = function(e) {
            const imageUrl = e.target.result;
            const upload_btn = document.getElementById(idname);
            upload_btn.style.backgroundImage = `url(${imageUrl})`;
        };

        reader.readAsDataURL(file);
    });
}