function Recover()
{
    var username = document.getElementById('username_textbox').value;
    var password = document.getElementById('password_textbox').value;
    var recoverpass = document.getElementById('recover_textbox').value;
    var Users_json = localStorage.getItem('UserCreds');
  
    if (Users_json === null){
        alert("Invalid");
        return
    }
    else {
        var users = JSON.parse(Users_json);
        var UserId = users.findIndex(p => p.username === username)
        if (UserId !== -1)
            {
                if (users[UserId].username === username && users[UserId].recovery === recoverpass)
                {
                    users[UserId].password = password;
                    localStorage.setItem('UserCreds', JSON.stringify(users));
                    window.location.href = "account.html?account=login"
                }
                else {
                    alert("Incorrect Recovery Password");
                }
            }
            else {
                alert("Incorrect");
            }   
        }
  
}

